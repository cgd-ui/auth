<template>
    <div class="login">
        <div class="box p-3">
            <div class="logo mb-3 is-flex">
                <figure class="image is-48x48  is-clickable is-flex"
                    @click="login">
                    <img src="/images/logo.svg"
                        :alt="meta.appName">
                </figure>
            </div>
            <form class="mb-2"
                @submit.prevent="submit()">
                <slot/>
                <submit v-bind="$attrs"
                    v-on="$listeners"/>
                <slot name="footer"/>
            </form>
        </div>
    </div>
</template>

<script>
import { mapState } from 'vuex';
import Errors from '@enso-ui/laravel-validation';
import Submit from './Submit.vue';

export default {
    name: 'AuthForm',

    components: { Submit },

    data: () => ({
        errors: new Errors(),
        state: {
            successful: false,
        },
    }),

    computed: {
        ...mapState(['meta']),
    },

    methods: {
         login() {
            if (this.$route.name !== 'login') {
                this.$router.push({ name: 'login' })
            }
        },
    },

    provide() {
        return {
            state: this.state,
            errors: this.errors,
        }
    },
};
</script>

